//swich expression
var day = 10;
switch (day) {
    case 1:
        console.log('Today is Monday');
        break;
    case 2:
        console.log('Today is Tuesday');
        break;
    case 3:
        console.log('Today is Wednesday');
        break;
    case 4:
        console.log('Today is Thusday');
        break;
    default:
        console.log('Invalid day value');
        break;
}
